package com.young.entity;

public class Collect {
    private int collect_id;
    private String collect_user;
    private String collect_book;

    public int getCollect_id() {
        return collect_id;
    }

    public void setCollect_id(int collect_id) {
        this.collect_id = collect_id;
    }

    public String getCollect_user() {
        return collect_user;
    }

    public void setCollect_user(String collect_user) {
        this.collect_user = collect_user;
    }

    public String getCollect_book() {
        return collect_book;
    }

    public void setCollect_book(String collect_book) {
        this.collect_book = collect_book;
    }
}
